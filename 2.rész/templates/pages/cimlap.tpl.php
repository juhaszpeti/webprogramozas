<h2 class="cim">Miért is jöttünk létre?</h2>
<p>A Kutyás Terápia céljára alapított Alapítványt a cégbíróság (Pk.60.156/2011, Megyei Nyt.sz.:230 ) jegyezte be KUTYÁVAL EGY MOSOLYÉRT ALAPÍTVÁNY néven. Közhasznú alapítványunk célja, a kutyás terápiát minél szélesebb körben elterjeszteni, az állatasszisztált terápia ezen formáját minél több gyermek fejlesztéséhez elérhetővé tenni, a pszichésen vagy fizikailag hátrányos helyzetű emberek állapotának, életminőségét javítani a segítő kutyák szerepének felhasználásával.</p>
<p>Pszichológusok, etológusok megfigyelték és tanulmányozták, hogy a kutya és más házi állatok hozzásegíthetik az embereket életminőségük javításához, egészségük helyreállításához.</p>
<p>A terápiás kutyák alkalmazása jótékonyan hat a gyermekek érzelmi fejlődésére. A kutya segítségével könnyebben alakítható ki személyes kapcsolat olyan emberekkel is, akik bizonyos betegségek miatt nehezebben lépnek kapcsolatba embertársaikkal vagy akár a terápiás szakemberrel.</p>
<p>2011 óta a Kutyával Egy Mosolyért Alapítvány vizsgázott kutya-felvezető párosai rendszeresen tartanak pedagógusokkal együttműködve fejlesztő órákat Speciális Iskolákban, segítjük kisiskolások tanulását, egészséges és beteg gyerekeknek tartunk kutyás foglalkozásokat, kórházi betegek gyógyulását segítjük, idősek napjait tesszük vidámabbá.</p>
<p>Szeretnénk megszervezni, megteremteni a hosszú távú terápiás munka feltételeit minél több felvezető-kutya párosnak, és így minél több gyerek és felnőtt számára elérhetővé tenni ezt a lehetőséget.</p>
